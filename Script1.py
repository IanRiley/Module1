in_oper = input("type your operation: ")
in_first = int(input("type your first: "))
in_sec = int(input("type your second:"))

if in_oper=="*":
    print (in_first * in_sec)
elif in_oper=="-":
    print (in_first-in_sec)
elif in_oper=="+":
    print (in_first+in_sec)
elif in_oper=="/":
    print (in_first/in_sec)
